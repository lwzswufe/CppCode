// 位运算符 

#include <iostream>
using std::cout; using std::endl;

int main()
{
    unsigned short x=3,y=5;                  //x=3(00000000 00000011)
                                             //y=5(00000000 00000101)
    cout<<"~x="<<(unsigned short)~x<<endl;   //位求反 = 65532
                                             // ~ 00000000 00000011
                                             //= 11111111 11111100 
    cout<<"~x="<<~x<<endl;                   //位求反 = -4
                                             // ~ 00000000 00000011
                                             //= 11111111 11111100      
    cout<<"x&y="<<(x&y)<<endl;               //位与  = 1
                                             //    00000000 00000011
                                             //  & 00000000 00000101
                                             //  = 00000000 00000001(1)
    cout<<"x^y="<<(x^y)<<endl;               //位异或 =6
                                             //    00000000 00000011
                                             //  ^ 00000000 00000101
                                             //  = 00000000 00000110(6)
    cout<<"x|y="<<(x|y)<<endl;               //位或  =7
                                             //    00000000 00000011
                                             //  | 00000000 00000101
                                             //  = 00000000 00000111(7)
    cout<<"x<<1="<<(x<<1)<<endl;             //位左移 =6
                                             //    00000000 00000011<<1
                                             //  = 00000000 00000110(6)
    cout<<"y>>1="<<(y>>1)<<endl;             //位右移 =2
                                             //    00000000 00000101>>1
                                             //  = 00000000 00000010(2)
    return 0;  
}